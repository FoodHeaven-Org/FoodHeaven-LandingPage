let currentLang = 'es';

function setLang(lang) {
  currentLang = I18N[lang] ? lang : 'es';
  document.documentElement.lang = currentLang;
  try { localStorage.setItem('fh-lang', currentLang); } catch (e) {}

  const dict = I18N[currentLang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if (dict[k] === undefined) return;
    if (el.tagName === 'META') el.setAttribute('content', dict[k]);
    else if (el.tagName === 'TITLE') el.textContent = dict[k];
    else if (/<[a-z!/]/i.test(dict[k])) el.innerHTML = dict[k];
    else el.textContent = dict[k];
  });

  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const k = el.getAttribute('data-i18n-ph');
    if (dict[k] !== undefined) el.setAttribute('placeholder', dict[k]);
  });

  document.querySelectorAll('.lang-flag button').forEach(b => {
    b.setAttribute('aria-pressed', b.dataset.lang === currentLang ? 'true' : 'false');
  });
}

function setTheme(t) {
  document.documentElement.setAttribute('data-theme', t);
  try { localStorage.setItem('fh-theme', t); } catch (e) {}
}

function initTheme() {
  let saved = null;
  try { saved = localStorage.getItem('fh-theme'); } catch (e) {}
  if (saved) return setTheme(saved);
  const prefersDark = window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches;
  setTheme(prefersDark ? 'dark' : 'light');
}

document.addEventListener('DOMContentLoaded', () => {
  initTheme();

  const themeBtn = document.getElementById('themeToggle');
  if (themeBtn) {
    themeBtn.addEventListener('click', () => {
      const cur = document.documentElement.getAttribute('data-theme');
      setTheme(cur === 'dark' ? 'light' : 'dark');
    });
  }

  let savedLang = 'es';
  try { savedLang = localStorage.getItem('fh-lang') || 'es'; } catch (e) {}
  setLang(savedLang);
  document.querySelectorAll('.lang-flag button').forEach(b => {
    b.addEventListener('click', () => setLang(b.dataset.lang));
  });

  const ham = document.getElementById('hamburger');
  const menu = document.getElementById('mobileMenu');
  function closeM() {
    ham.classList.remove('open'); menu.classList.remove('open');
    ham.setAttribute('aria-expanded', 'false'); menu.setAttribute('aria-hidden', 'true');
    document.body.style.overflow = '';
  }
  function openM() {
    ham.classList.add('open'); menu.classList.add('open');
    ham.setAttribute('aria-expanded', 'true'); menu.setAttribute('aria-hidden', 'false');
    document.body.style.overflow = 'hidden';
  }
  if (ham && menu) {
    ham.addEventListener('click', () => menu.classList.contains('open') ? closeM() : openM());
    menu.querySelectorAll('a').forEach(a => a.addEventListener('click', closeM));
  }

  const hdr = document.getElementById('header');
  const onScroll = () => hdr && hdr.classList.toggle('scrolled', window.scrollY > 8);
  document.addEventListener('scroll', onScroll, { passive: true });
  onScroll();

  document.querySelectorAll('.faq-q').forEach(q => {
    q.addEventListener('click', () => {
      const item = q.closest('.faq-item');
      const answer = item.querySelector('.faq-a');
      const isOpen = item.classList.contains('open');
      document.querySelectorAll('.faq-item.open').forEach(other => {
        if (other !== item) {
          other.classList.remove('open');
          other.querySelector('.faq-a').style.maxHeight = null;
          other.querySelector('.faq-q').setAttribute('aria-expanded', 'false');
        }
      });
      if (isOpen) {
        item.classList.remove('open');
        answer.style.maxHeight = null;
        q.setAttribute('aria-expanded', 'false');
      } else {
        item.classList.add('open');
        answer.style.maxHeight = answer.scrollHeight + 'px';
        q.setAttribute('aria-expanded', 'true');
      }
    });
  });

  const contactForm = document.getElementById('contactForm');
  if (contactForm) {
    const success = document.getElementById('contactSuccess');
    contactForm.addEventListener('submit', (e) => {
      e.preventDefault();
      const name = contactForm.querySelector('#cf-name');
      const email = contactForm.querySelector('#cf-email');
      const message = contactForm.querySelector('#cf-msg');
      const emailOk = /.+@.+\..+/.test((email.value || '').trim());
      if (!name.value.trim() || !emailOk || !message.value.trim()) {
        if (success) {
          success.hidden = false;
          success.classList.add('is-error');
          success.textContent = (I18N[currentLang] || I18N.es)['contact.error'];
        }
        return;
      }
      if (success) {
        success.hidden = false;
        success.classList.remove('is-error');
        success.textContent = (I18N[currentLang] || I18N.es)['contact.success'];
      }
      contactForm.reset();
    });
  }

  const testiCards = document.querySelectorAll('.testi-card');
  if (testiCards.length) {
    let idx = 0;
    const rotate = (dir) => {
      idx = (idx + dir + testiCards.length) % testiCards.length;
      testiCards.forEach((c, i) => {
        c.style.opacity = i === idx ? '1' : '.55';
        c.style.transform = i === idx ? 'scale(1.03)' : 'scale(1)';
        c.style.transition = 'opacity .4s ease, transform .4s ease';
      });
    };
    rotate(0);
    const left = document.querySelector('.testi-arrow.left');
    const right = document.querySelector('.testi-arrow.right');
    if (left) left.addEventListener('click', () => rotate(-1));
    if (right) right.addEventListener('click', () => rotate(1));
  }

  const reduce = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
  if (!reduce && 'IntersectionObserver' in window) {
    const io = new IntersectionObserver(entries => {
      entries.forEach(e => {
        if (e.isIntersecting) { e.target.classList.add('in'); io.unobserve(e.target); }
      });
    }, { threshold: 0.05, rootMargin: '0px 0px -6% 0px' });
    document.querySelectorAll('.reveal').forEach(el => io.observe(el));
  } else {
    document.querySelectorAll('.reveal').forEach(el => el.classList.add('in'));
  }
});
